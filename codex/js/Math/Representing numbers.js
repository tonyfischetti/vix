// js, math, representing numbers, binary, octal, thousands separator, decimal to binary string

/*
 * All numbers in Javascript are double floats
 */

2_110_000;      // 2,110,000
2.11e6;         // 2,110,000
1.1e-1;         // 0.11

// binary
0b10001;        // 17

// hex
0x0e;           // 14

// octal
0o010;          // 8


// as strings
(17).toString(2);   // '10001'
(8).toString(8);    // '10'
(14).toString(16);  // 'e'

